#include <iostream>
using namespace std;

int main()
{
    // Initializing variables.
    int a = 1690;
    int b = 7;
    int c = 20;
    int d = 5;

    // Performing division operations in C++.
    int result = a / b;
    int result2 = c / d;

    // Displaying Results.
    cout << a << " / " << b << " = " << result << endl;
    cout << c << " / " << d << " = " << result2 << endl;
}
